  WHERE (("ic"."item_cat_id" = "item"."cat_id") AND ("r"."UID" = "auth"."uid"()) AND ((("r"."role")::"text" = 'Admin'::"text") OR (("r"."role")::"text" = 'Owner'::"text"))))));



