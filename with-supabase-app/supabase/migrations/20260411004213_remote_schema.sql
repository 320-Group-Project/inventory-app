CREATE OR REPLACE FUNCTION "public"."add_owner_on_club_create"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
begin
  insert into "Role" ("UID", club_id, role)
  values (auth.uid(), new.club_id, 'Owner');

  return new;
end;
$$;


